#!/usr/bin/env node
// Read-only export helper. Run manually + occasionally to re-capture the
// catalog (categories / subcategories / starter_habits) from PROD into the
// seed migration supabase/migrations/043_seed_catalog.sql.
//
// STRICTLY READ-ONLY ON PROD: pg_dump issues SELECTs only — never writes to
// prod. The only thing written is the local .sql file.
//
// Output is an idempotent, column-qualified INSERT seed
// (--column-inserts + --on-conflict-do-nothing) so `supabase db push`
// distributes it to every env as a harmless no-op on rows that already exist.
//
// FK ORDER IS GUARANTEED HERE by dumping each table in a SEPARATE pg_dump and
// concatenating in dependency order (categories -> subcategories ->
// starter_habits). pg_dump does NOT topologically sort -t tables, and
// `supabase db push` runs the file in one transaction, so a child INSERT
// before its parent would abort the migration. Per-table + fixed order avoids
// that entirely — no manual reordering needed.
//
// Drift is additive-only (DO NOTHING never UPDATEs). To correct prod edits,
// write a NEW forward migration (044+) — never edit 043 once applied.
//
//   node scripts/export-catalog-seed.mjs
//
// Env (in .env.local or the shell):
//   PROD_DATABASE_URL — prod connection string, DIRECT port 5432
//     (db.<ref>.supabase.co:5432), NOT the 6543 PgBouncer pooler.
//     Passed to pg_dump via PGDATABASE env (kept out of argv / `ps`).

import { spawnSync } from 'child_process';
import { resolve, dirname } from 'path';
import { fileURLToPath } from 'url';
import { writeFileSync } from 'fs';
import { config } from 'dotenv';

const __dirname = dirname(fileURLToPath(import.meta.url));
config({ path: resolve(__dirname, '../.env.local') });

const PROD_DATABASE_URL = process.env.PROD_DATABASE_URL;
if (!PROD_DATABASE_URL) {
  console.error(
    'Refusing to run: PROD_DATABASE_URL is unset.\n' +
      'Set it to the prod DIRECT connection (port 5432), e.g.\n' +
      '  postgresql://postgres:<pwd>@db.<ref>.supabase.co:5432/postgres\n' +
      '(Use the Direct connection, NOT the 6543 PgBouncer transaction pooler.)'
  );
  process.exit(1);
}
if (PROD_DATABASE_URL.includes(':6543')) {
  console.error(
    'Refusing to run: PROD_DATABASE_URL points at port 6543 (PgBouncer pooler).\n' +
      'pg_dump needs a session-level connection — use the DIRECT port 5432.'
  );
  process.exit(1);
}

// Dependency order is load-bearing — parent before child.
const TABLES = ['categories', 'subcategories', 'starter_habits'];
const outFile = resolve(__dirname, '../supabase/migrations/043_seed_catalog.sql');

// Read-only: pg_dump only. No --clean/--create/--if-exists — nothing destructive.
// Connection passed via PGDATABASE (libpq accepts a URI), keeping it out of argv.
const env = { ...process.env, PGDATABASE: PROD_DATABASE_URL };

function dumpTable(table) {
  const args = [
    '--data-only',
    '--column-inserts',
    '--on-conflict-do-nothing',
    '--no-owner',
    '--no-privileges',
    '--no-comments',
    '--schema=public',
    '-t',
    `public.${table}`,
  ];
  const res = spawnSync('pg_dump', args, { encoding: 'utf8', env, stdio: ['ignore', 'pipe', 'inherit'] });
  if (res.error) {
    console.error(`pg_dump failed to start: ${res.error.message}`);
    process.exit(1);
  }
  if (res.status !== 0) {
    console.error(`pg_dump exited with code ${res.status} for ${table}.`);
    process.exit(res.status ?? 1);
  }
  return res.stdout;
}

console.log('Running pg_dump (read-only) against PROD…');
const blocks = TABLES.map((t) => {
  console.log(`  - public.${t}`);
  return `-- ${t}\n${dumpTable(t).trim()}\n`;
});

const header =
  '-- 043_seed_catalog.sql — GENERATED by scripts/export-catalog-seed.mjs.\n' +
  '-- Idempotent catalog seed (categories -> subcategories -> starter_habits).\n' +
  '-- ON CONFLICT DO NOTHING: no-op where rows already exist. Do not hand-edit;\n' +
  '-- re-run the exporter, or add a forward migration (044+) for changes.\n\n';

writeFileSync(outFile, header + blocks.join('\n'), 'utf8');

console.log('\nDone. Wrote idempotent, FK-ordered seed to:');
console.log(`  ${outFile}`);
console.log('Check `git status` for stray iCloud " 2" copies before committing.');
